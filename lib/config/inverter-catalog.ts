/** All inverters: residential BOM units first, then iSolarBP commercial */
export const INVERTER_CATALOG: InverterSpec[] = [
  ...KSOLAR_RESIDENTIAL,
  ...ISOLAR_COMMERCIAL,
];

/** Only the 13 residential inverters used in KSolar BOM quotes */
export const RESIDENTIAL_INVERTERS: InverterSpec[] = KSOLAR_RESIDENTIAL;

/** Only the 19 iSolarBP commercial inverters */
export const COMMERCIAL_INVERTERS: InverterSpec[] = ISOLAR_COMMERCIAL;

/** Convenience: look up an inverter by id */
export function findInverter(id: string): InverterSpec | undefined {
  return INVERTER_CATALOG.find((inv) => inv.id === id);
}

/**
 * Filter residential inverters for a specific topology.
 * @example filterResidentialInverters("1P", "ongrid") // → 3 on-grid 1P units
 */
export function filterResidentialInverters(
  phase: InverterPhase,
  mode: InverterMode
): InverterSpec[] {
  return KSOLAR_RESIDENTIAL.filter(
    (inv) => inv.phase === phase && inv.mode === mode
  );
}
