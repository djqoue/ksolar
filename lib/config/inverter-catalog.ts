/**
 * Inverter catalog for KSolar.
 *
 * Two segments:
 *   "ksolar-bom"  — residential 3–20 kW inverters used in KSolar BOM quotes.
 *                   Prices come from bom-catalog.ts market estimates (THB).
 *                   Technical specs are representative datasheets values.
 *   "isolar-bp"   — commercial 30–320 kW inverters scraped from iSolarBP
 *                   设计物料管理 > 逆变器 (2026-04-20). Prices in CNY → THB.
 *
 * Manufacturer note: all iSolarBP inverters are Sungrow (阳光电源).
 * KSolar-BOM residential inverters use the Growatt SUN-K series.
 */
import { cnyToThb } from "@/lib/config/currency";

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export type InverterPhase = "1P" | "3P";
export type InverterMode = "ongrid" | "hybrid";
export type InverterSource = "ksolar-bom" | "isolar-bp";

export interface InverterSpec {
  /** Unique kebab-case slug */
  id: string;
  /** Data origin */
  source: InverterSource;
  manufacturer: string;
  model: string;
  /** Grid phase */
  phase: InverterPhase;
  /** ongrid = grid-tie only; hybrid = supports battery */
  mode: InverterMode;
  /** Rated AC output power (kW) */
  ratedPowerKW: number;
  /** CNY price from iSolarBP; null = not listed or not applicable */
  priceCNY: number | null;
  /** THB price; null = not listed */
  unitCostTHB: number | null;
  // --- DC input ---
  maxInputVoltageV: number;
  mpptVoltageMinV: number;
  mpptVoltageMaxV: number;
  mpptCount: number;
  maxStringCount: number;
  maxInputCurrentA: number;
  // --- AC output ---
  maxOutputCurrentA: number;
  /** Rated grid voltage (V) — 230 for 1P, 400 for 3P */
  ratedGridVoltageV: number;
  // --- Physical ---
  /** Max DC input cable cross-section (mm²) */
  maxCableMM2: number | null;
  dimWidthMm: number | null;
  dimDepthMm: number | null;
  dimHeightMm: number | null;
  weightKg: number | null;
}

